package hk.com.novare.tempoplus.accountsystem;


public class HrDataAccessException extends Exception {

	public HrDataAccessException() {
		super();
	}

	public HrDataAccessException(String message, Throwable cause) {
		super(message, cause);
	}

	public HrDataAccessException(String message) {
		super(message);
	}

	public HrDataAccessException(Throwable cause) {
		super(cause);
	}

}
